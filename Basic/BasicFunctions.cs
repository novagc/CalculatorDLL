namespace Basic
{
    public class BasicFunctions
    {
        public static int PlusInt(int f, int s) => f + s;

        public static int MinusInt(int f, int s) => f - s;

        public static int DivideInt(int f, int s) => f / s;

        public static int MultiplyInt(int f, int s) => f * s;

        public static double PlusDouble(double f, double s) => f + s;

        public static double MinusDouble(double f, double s) => f - s;

        public static double DivideDouble(double f, double s) => f / s;

        public static double MultiplyDouble(double f, double s) => f * s;
    }
}
